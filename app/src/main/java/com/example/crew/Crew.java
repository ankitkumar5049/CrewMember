package com.example.crew;

public class Crew {
    public String CrewName;
    public String CrewAgency;
    public String CrewImage;

    public String getCrewImage() {
        return CrewImage;
    }

    public void setCrewImage(String crewImage) {
        CrewImage = crewImage;
    }

    public String CrewWiki;
    public String CrewActive;

    public String getCrewName() {
        return CrewName;
    }

    public void setCrewName(String crewName) {
        CrewName = crewName;
    }

    public String getCrewAgency() {
        return CrewAgency;
    }

    public void setCrewAgency(String crewAgency) {
        CrewAgency = crewAgency;
    }

    public String getCrewWiki() {
        return CrewWiki;
    }

    public void setCrewWiki(String crewWiki) {
        CrewWiki = crewWiki;
    }

    public String getCrewActive() {
        return CrewActive;
    }

    public void setCrewActive(String crewActive) {
        CrewActive = crewActive;
    }


}
